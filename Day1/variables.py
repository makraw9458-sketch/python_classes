# Identifiers
# Rules for identifiers

# Allowed characters: Use only alphanumeric characters (a-z, A-Z, 0-9) and underscores (_).
# No starting digits: The name cannot begin with a number.
# No reserved keywords: Standard keywords like if, else, for, class, or def cannot be used.
# No special symbols: Characters like @, $, #, %, or spaces are strictly prohibited.
# Case sensitivity: Python treats letter casing strictly; user_age, User_Age, and USER_AGE are three completely different identifiers.
# No length limits: An identifier can theoretically be as long as you want.


